@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    Text(
        text = "Hello $name!",
        modifier = modifier
    )
}

@Composable
fun RestaurantName(name: String) {
    Text(text = name)
}

@Composable
fun RestaurantName(name: String, size: Int) {
    Text(
        text = name, fontSize = size.sp
    )
}

